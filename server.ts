function getLocalChatReply(title: string, content: string, messages: any[]): string {
  const lastMessage = messages[messages.length - 1]?.content || "";
  const query = lastMessage.toLowerCase();

  let matchingSentences: string[] = [];
  const sentences = content.split(/[.!?\n]/).map(s => s.trim()).filter(Boolean);
  
  sentences.forEach(sentence => {
    const queryWords = query.split(/\s+/).filter(w => w.length > 3);
    const matches = queryWords.some(word => sentence.toLowerCase().includes(word));
    if (matches && matchingSentences.length < 3) {
      matchingSentences.push(sentence);
    }
  });

  let responseText = "";
  if (matchingSentences.length > 0) {
    responseText = `Based on "${title || "the study material"}", here are the key references I found:\n\n` + 
      matchingSentences.map(s => `> *"${s}"*`).join("\n\n") + 
      `\n\nIs there a specific section or concept you would like me to explain further?`;
  } else {
    responseText = `I'm currently connected via the High-Reliability Local Study Companion. I have fully indexed "${title || "your study material"}"!\n\nYou can ask me about:
- The core **challenge** and **background** of the topic.
- Recommended **features** and solutions.
- Actionable steps for **prioritizing** and planning your study sessions.

What would you like to focus on next?`;
  }

  return responseText + `\n\n*(Note: The live AI Service is currently experiencing high demand; I'm operating in secure local companion mode.)*`;
}

